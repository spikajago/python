import turtle


for i in range(2):
    turtle.fd(120)
    turtle.lt(90)
    turtle.fd(50)
    turtle.lt(90)


turtle.penup()
turtle.goto(120,50)
turtle.pendown()


for i in range(2):
    turtle.fd(120)
    turtle.lt(90)
    turtle.fd(50)
    turtle.lt(90)


turtle.right(90)
for i in range(2):
    turtle.fd(120)
    turtle.lt(90)
    turtle.fd(50)
    turtle.lt(90)


turtle.right(90)
for i in range(2):
    turtle.fd(50)
    turtle.rt(90)
    turtle.fd(120)
    turtle.rt(90)

turtle.done()